// mathUtils.js - A module with some math utility functions

// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Function to multiply two numbers
function multiply(a, b) {
    return a * b;
}

// Function to square a number
function square(x) {
    return x * x;
}

// Exporting the functions
module.exports = {
    add,
    multiply,
    square
};
